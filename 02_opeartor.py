# 案例演示: Python的所有运算符

# 1、算数运算符
a = 10
b = 3

# 加法
addition = a + b
print(f"加法结果：{addition}")

# 减法
subtraction = a - b
print(f"减法结果：{subtraction}")

# 乘法
multiplication = a * b
print(f"乘法结果：{multiplication}")

# 除法
division = a / b
print(f"除法结果：{division}")

# 取整除法
floor_division = a // b
print(f"取整除法结果：{floor_division}")

# 取模 就是取余
modulo = a % b
print(f"取模结果：{modulo}")

# 幂运算  ==> a的b次方
power = a ** b
print(f"取模结果：{power}")

# 2、比较运算符
print(f"等于比较: {a == b}")
print(f"不等于比较: {a != b}")
print(f"大于比较: {a > b}")
print(f"小于比较: {a < b}")
print(f"大于等于比较: {a >= b}")
print(f"小于等于比较: {a <= b}")

# 3、赋值运算符
# 前面的加减乘除等等数学运算符，都兼容这些 什么等于，如+=，-=，*= ...
c = 5
c += a  # 等同于 c = c + a
print(f"加法赋值后 c 的值: {c}")

# 4、逻辑运算 即 与或非
x = True
y = False
print(f"逻辑与： {x and y}")
print(f"逻辑或： {x or y}")
print(f"逻辑非： {not x}")

